#include<stdio.h> 
int main() 
{
	int a = 20;  //4个字节-32个bit
	//00000000000000000000000000010100  -原码
	//00000000000000000000000000010100  -反码 
	//00000000000000000000000000010100  -补码    对应16进制：0x00000014 （0x表示16进制） 
//在内存中的表示方式：以十六进制&&小端字节序储存  14 00 00 00  
	 
	int b = -10;
	//10000000000000000000000000011010  -原码
	//11111111111111111111111111100101  -反码
	//11111111111111111111111111100110  -补码   对应16进制：0xffffffe6 
//在内存中的表示方式：以十六进制&&小端储存  e6 ff ff ff 
	
	return 0;
}


/*注意 ！！
32个bit时， 00000000000000000000000000010100（二进制）这32个数字中，一个数字放进一个bit位里
4个字节时， 14 00 00 00（十六进制）把两个数字放进一个字节中    IM！！！ 
*/

