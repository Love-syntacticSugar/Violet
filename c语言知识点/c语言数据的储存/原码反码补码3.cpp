#include<stdio.h>
int main()
{
	char a = -128;
	/* 	打印%d 
		1. 10000000000000000000000010000000 原码
	       11111111111111111111111101111111 反码
		   11111111111111111111111110000000 补码 
		2. 10000000 						 计算机取后8位
		3.取出
		4. 11111111111111111111111110000000 整形提升 
		5.观察到打印%d
		6.求出原码 11111111111111111111111101111111 反码
		           10000000000000000000000010000000 原码 
		7.解出对应十进制数：-128 
	*/
	/*
		打印%u
		1.  10000000000000000000000010000000 原码
			11111111111111111111111101111111 反码
			11111111111111111111111110000000 补码 
		2.  10000000 
		3.取出
		4.  11111111111111111111111110000000	将取出值根据定义的类型char进行整形提升
		5.观察到要打印%u
		6.求出原码为：11111111111111111111111110000000
		7.转化为十进制数字：4294967168 
	*/
		
	printf("%d\n",a);
	printf("%u\n",a);
	//%d - 打印十进制有符号数字
	//%u - 打印十进制无符号数字
	return 0;
}
