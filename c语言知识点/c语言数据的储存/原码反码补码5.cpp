#include<stdio.h>
int main()
{
	int i = -20;
	unsigned int j = 10;
	printf("%d\n",i + j);
	return 0;
}

/*  解：
	i的补码：10000000000000000000000000010100 	原
			 11111111111111111111111111101011	反
			 11111111111111111111111111101100	补
	j的补码：00000000000000000000000000001010	原反补相同
i+j补码相加：11111111111111111111111111110110	i+j的补码
	由于打印 %d 所以需要将i+j的补码转化为原码
			 11111111111111111111111111110101   i+j的反码
			 10000000000000000000000000001010	i+j的原码
		解得：-10 
*/
			   
