#include<stdio.h>
int main()
{
	int a = 1;
	
	int b = 1; 
	
	a = a << 1;             //1.移位操作符中移位移的是二进制位（原码）    2.注意不能 a << 1而要 a = a << 1（这个不像a++）    3.原码原码原码，重要的事情说三遍！！不是反码！！
						    //1的二进制为000000000000000000000000000001   则最左边的0移掉1位，最后面补0   即000000000000000000000000000010 
	b = b >> 1;				// 同理<< 
	
	printf("%d\n",a);
	
	printf("%d\n",b);
	
	return 0;
}


